package org.book.chapter08;

public class HowToUsePow {
    public static void main(String[] args) {
        int a = 7;
        int b = 5;
        int c = 2;
        System.out.println(a * Math.pow(10, 1));
        System.out.println(b * Math.pow(10, 2));
        System.out.println(c * Math.pow(10, 3));
    }
}
