package org.book.etc;

public class SwitchCaseBreak {
    public static void main(String[] args) {
        int n = 100;

        switch (n){
            case 100:
                System.out.println("100입니다.");
            case 200:
                System.out.println("200입니다.");
            case 300:
                System.out.println("300입니다.");
        }
    }
}
