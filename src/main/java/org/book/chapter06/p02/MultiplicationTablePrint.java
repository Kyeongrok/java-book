package org.book.chapter06.p02;

public class MultiplicationTablePrint {
    public static void main(String[] args) {
        System.out.printf("2 * 1 = 2\n");
        System.out.printf("2 * 2 = 4\n");
        System.out.printf("2 * 3 = 6\n");

        for (int i = 2; 1 == 1; i++) {
            System.out.println(i);
        }
    }
}
